public class A1IntroToJava {
    /*
    Object-oriented programming concepts:

    JAVA is an object-oriented programming language.

    1.OBJECT - is a software bundle (пучок) of related state and behavior.Software objects are used to model real-world
        objects that you find in everyday life.
        Objects have state (stored in fields/variables) and behavior(stored in methods/functions).
        Hiding internal state and requiring all interaction to be performed through an object's methods is known as
        DATA ENCAPSULATION - a fundamental principle of object-oriented programming.

    2. CLASS - is a blueprint (план, проект) or prototype from which objects are created.
        Each object in a class is built (created) from the same set of blueprints and therefore contains the same components.
        In programming, it is said that created object (f.e.bicycle) is an INSTANCE of the class of objects known as Bicycles.
        Class that does not contain main method is a BLUEPRINT; Class that contains main method is an APPLICATION.

    3. INHERITANCE - provides a powerful and natural mechanism for organizing and structuring your software.
        Several subclasses can have common states and behaviors that can be created in one superclass.

    4. INTERFACE - is a contract between a class and the outside world. When a class implements an interface, it promises
    to provide the behavior published by that interface.

    5. PACKAGE - is a namespace for organizing classes and interfaces in a logical manner. Java platform provides an enormous
        class library (a set of packages) suitable in your own applications. This library is known as   APPLICATION
        PROGRAMMING INTERFACE (API).
        The Java Platform API specification contains the complete listing of all packages, interfaces, classes, fields,
        and methods supplied by the Java SE platform - it will be your most important piece of reference documentation.
   ==========================================================================================================

        QUESTIONS WITH ANSWERS:
        1. Real-world objects contain state and behavior.
        2. A software object's state is stored in fields/variables.
        3. A software object's behavior is exposed through methods.
        4. Hiding internal data from the outside world, and accessing it only through publicly exposed methods is known
        as data encapsulation.
        5. A blueprint for a software object is called a class.
        6. Common behavior can be defined in a superclass and inherited into a subclass using the 'extends' keyword.
        7. A collection of methods with no implementation is called an interface.
        8. A namespace that organizes classes and interfaces by functionality is called a package.
        9. The API stands for Application Programming Interface.

     */
}
